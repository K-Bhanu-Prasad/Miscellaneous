package com.cg;

/**
 * @author bkuraman
 *
 */
public class Point {

  private int x;

  private int y;

  /**
   * @return x
   */
  public int getX() {

    return this.x;
  }

  /**
   * @param x new value of {@link #getx}.
   */
  public void setX(int x) {

    this.x = x;
  }

  /**
   * @return y
   */
  public int getY() {

    return this.y;
  }

  /**
   * @param y new value of {@link #gety}.
   */
  public void setY(int y) {

    this.y = y;
  }

}
